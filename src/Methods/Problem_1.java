//Write a program to calculate the square and cube of numbers 2, 3, and 4.
//
//Sample 1:
//Input
//        Output
//
//4
//        8
//        9
//        27
//        16
//        64

package Methods;

public class Problem_1 {
    public static void main (String[] args)
    {
        int a = 2;
        int b = 3;
        int c = 4;

        System.out.println((int)Math.pow(a, 2));
        System.out.println((int)Math.pow(a, 3));
        System.out.println((int)Math.pow(b, 2));
        System.out.println((int)Math.pow(b, 3));
        System.out.println((int)Math.pow(c, 2));
        System.out.println((int)Math.pow(c, 3));

    }
}
