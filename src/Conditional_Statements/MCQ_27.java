public class MCQ_27 {
    public static void main(String[] args) {
        int a = 10;
        int b = 10;

        if (a > b) {
            System.out.println("a is greater.");
        } else if (b > a) {
            System.out.println("b is greater.");
        } else {
            System.out.print("a is equal to b.");
        }

        System.out.println(" Program ends");
    }
}
