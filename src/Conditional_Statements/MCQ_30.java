package Conditional_Statements;

public class MCQ_30 {
    public static void main(String[] args) {
        int a = 0;
        int b = -10;

        if (a >= b) {
            System.out.print("a is greater or equal to b. ");
        }
        if (a == 0) {
            System.out.print("a is 0. ");
        }
        if (a <= 5) {
            System.out.print("a is not more than 5. ");
        }

        System.out.print("Program ends ");
    }
}
